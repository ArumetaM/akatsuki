# ============================================================
# タイムアウト設定（ミリ秒）
# ============================================================

class Timeouts:
    """待機時間の定数クラス"""

    # 基本的な待機時間
    SHORT = 1000          # 短い待機（モーダルクローズなど）
    MEDIUM = 2000         # 中程度の待機（ページ遷移後など）
    NAVIGATION = 3000     # ナビゲーション後の待機
    LONG = 4000           # 長い待機（重い処理後など）
    SELECTOR_WAIT = 5000  # セレクタ待機のタイムアウト
    VERY_LONG = 6000      # 非常に長い待機
    LOGIN = 8000          # ログインボタン後の待機
    NETWORKIDLE = 10000   # ネットワークアイドル待機
    BALANCE_CHECK = 30000 # 残高反映確認の待機（最大30秒）


